# Tipo variável: int, float, srt(string)
numero1 = 1
print(numero1)
print("O tipo de variável do numero é: ", type(numero1))

numero2 = 7.4
print(numero2)
print("O tipo de variável do numero é: ", type(numero2))

numero3 = 'vera'
print(numero3)
print("O tipo de variável do numero é: ", type(numero3))

# soma
soma = numero1 + numero2
print(soma)
print("O tipo de variável do numero é: ", type(soma))

# Booleano
trouxe_carteira = False
print("O tipo de variável de trouxe_carteira é: ", type(trouxe_carteira))

# Aspas dulas
texto = '"Na madrugada"'
print(texto)